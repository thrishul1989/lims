package com.todaysoft.lims.sample.model.order;

public class OrderSampleModel
{
    private String testingSampleId;
    
    private String code;
    
    private String type;
    
    private String purpose;
    
    public String getTestingSampleId()
    {
        return testingSampleId;
    }
    
    public void setTestingSampleId(String testingSampleId)
    {
        this.testingSampleId = testingSampleId;
    }
    
    public String getCode()
    {
        return code;
    }
    
    public void setCode(String code)
    {
        this.code = code;
    }
    
    public String getType()
    {
        return type;
    }
    
    public void setType(String type)
    {
        this.type = type;
    }
    
    public String getPurpose()
    {
        return purpose;
    }
    
    public void setPurpose(String purpose)
    {
        this.purpose = purpose;
    }
    
}
