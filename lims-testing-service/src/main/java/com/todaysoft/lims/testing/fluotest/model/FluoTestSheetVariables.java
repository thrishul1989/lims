package com.todaysoft.lims.testing.fluotest.model;

public class FluoTestSheetVariables
{
    private String reagentKitId;
    
    private String testId;
    
    private String description;

   

    public String getReagentKitId()
    {
        return reagentKitId;
    }

    public void setReagentKitId(String reagentKitId)
    {
        this.reagentKitId = reagentKitId;
    }

    public String getTestId()
    {
        return testId;
    }

    public void setTestId(String testId)
    {
        this.testId = testId;
    }

    public String getDescription()
    {
        return description;
    }

    public void setDescription(String description)
    {
        this.description = description;
    }
}
