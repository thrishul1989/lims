package com.todaysoft.lims.system.modules.bpm.firstpcr.model;


public class FirstPCRAssignableTaskSearcher
{
    private String sampleCode;
    
    private String primerCode;
    
    private String chrLocation;
    
    private String combineCode;
    
    public String getSampleCode()
    {
        return sampleCode;
    }
    
    public void setSampleCode(String sampleCode)
    {
        this.sampleCode = sampleCode;
    }
    
    public String getPrimerCode()
    {
        return primerCode;
    }
    
    public void setPrimerCode(String primerCode)
    {
        this.primerCode = primerCode;
    }
    
    public String getChrLocation()
    {
        return chrLocation;
    }
    
    public void setChrLocation(String chrLocation)
    {
        this.chrLocation = chrLocation;
    }
    
    public String getCombineCode()
    {
        return combineCode;
    }
    
    public void setCombineCode(String combineCode)
    {
        this.combineCode = combineCode;
    }
}
