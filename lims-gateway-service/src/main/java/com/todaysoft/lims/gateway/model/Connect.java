package com.todaysoft.lims.gateway.model;

public class Connect {

	private String id;
	
	private String connectNum;//接头号

	private String connectSequence;//接头序列

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getConnectNum() {
		return connectNum;
	}

	public void setConnectNum(String connectNum) {
		this.connectNum = connectNum;
	}

	public String getConnectSequence() {
		return connectSequence;
	}

	public void setConnectSequence(String connectSequence) {
		this.connectSequence = connectSequence;
	}
	
	
}
